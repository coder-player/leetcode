public class Offer_04 {

    public static void main(String[] args) {

        int[][] matrix = {
                {1, 4, 7, 11, 15},
                {2, 5, 8, 12, 19},
                {3, 6, 9, 16, 22},
                {10, 13, 14, 17, 24},
                {18, 21, 23, 26, 30}
        };
        int target = 5;
        System.out.println(findNumberIn2DArray(matrix, target));
    }

    public static boolean findNumberIn2DArray(int[][] matrix, int target) {
        if (matrix.length == 0) {
            return false;
        }
        int row = 0;
        int col = matrix[0].length - 1;
        return dfs(matrix, row, col, target);
    }

    //从右上角开始dfs遍历【二叉搜索树】
    public static boolean dfs(int[][] matrix, int row, int col, int target) {
        if (row > matrix.length - 1 || col < 0) { //边界判断
            return false;
        }
        if (target > matrix[row][col]) { //大于当前位置向下扫描
            return dfs(matrix, row + 1, col, target);
        } else if (target < matrix[row][col]) { //小于当前位置向左扫描
            return dfs(matrix, row, col - 1, target);
        }
        return true;
    }
}
